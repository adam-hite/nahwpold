        <h1>NAHBPC returns to it's birthplace</h1>
        <p>Seattle, the birthplace of hardcourt bike polo, is set to host the 2019 North American Hardcourt Bike Polo Championship, one of the most competitive and prestigious bike polo tournaments in the&nbsp;world.</p><br><br>
        <p><strong>Judkins Park</strong><br>1119 23rd Ave S, Seattle, WA 98144</p><br>
        <div class="embed-container"><iframe src="https://www.google.com/maps/embed?pb=!1m14!1m8!1m3!1d2690.8085539219933!2d-122.3031156!3d47.5909657!3m2!1i1024!2i768!4f13.1!3m3!1m2!1s0x0%3A0x4bbe609dbe6cb0f7!2sJudkins+Park+Street+Hockey+%2F+Futsal+%2F+Bike+Polo+Courts!5e0!3m2!1sen!2sus!4v1563465812870!5m2!1sen!2sus" width="914" height="514" allowfullscreen title="Map of Judkins Park"></iframe></div>
        <div class="schedule">
          <h2>Schedule &amp; Format</h2>
          <div>
            <h5>Friday July 26th</h5>
            <p><strong>Phase 1</strong>: <em>2 groups of 9 teams, round robin play</em></p>
            <p>The field of 18 teams will be divided and balanced into 2 groups (see groups below). Teams will play 4 matches on Day 1.</p>
          </div>
          <div>
            <h5>Saturday, July 27th</h5>
            <p><strong>Phase 1</strong>: <em>continued from Day 1</em></p>
            <p>Teams will remain in their same groups and play the remaining 4 matches. At completion of Day 2, all teams in a group will have completed a round robin and will have played every other team in their group. The top 4 teams in each group advance to Phase 2.</p>
          </div>
          <div>
            <h5>Sunday, July 28th</h5>
            <p><strong>Phase 2</strong>: <em>2 groups of 4, point-based competition</em></p>
            <p>The top 8 teams will be divided into 2 groups. Teams play 3 matches. At completion, each team’s Phase 2 record (win/lose/tie, goal differential, etc) will determine ranking. The top 2 teams from each group will advance to phase 3.</p>
            <p><strong>Phase 3</strong>: <em>Single Elimination Bracket</em></p>
            <p>The top 4 teams will be seeded into a bracket based on their Phase 2 results. All ties at the end of regulation time will be played out in overtime until golden goal.</p>
          </div>
