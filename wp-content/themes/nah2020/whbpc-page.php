                    <div class="left">
                        <h2>Travel Information</h2>
                        <p>A map and wristband will be provided to participants once they have check-in at the registration booth (location &amp; time TBA).</p>
                        <p><strong>HOTEL:</strong> BOOK NOW! Accommodations are filing up. The Courtyard (<a href="https://goo.gl/maps/uiWefWZor3E2">775 Newtown Court Lexington, KY 40511</a>) has provided a block of rooms through the link below. <strong>You must book before September.</strong> <a href="http://www.marriott.com/meeting-event-hotels/group-corporate-travel/groupCorp.mi?resLinkData=Bike%20Polo%20World%20Championships%20Room%20Block%20%20%5ELEXNO%60BPCBPCA%7CBPCBPCB%60129.00%60USD%60false%604%6010/1/17%6010/8/17%609/3/17&app=resvlink&stop_mobi=yes">Book now using this link!</a></p>
                        <p>Otherwise, <a href="http://www.qualityinnlexingtonky.com/">Quality Inn Northwest</a> has a few rooms left. Both of these hotels are on <a href="http://www.kentuckytourism.com/outdoor-adventure/attraction/the-legacy-trail/746/">The Legacy Trail</a> which takes you straight to the courts.</p>
                        <p><strong>CAMPING</strong> Camping is available by reservation at the <a href="//kyhorsepark.com/visit/park-info-resources/khp-campground">KY Horse Park</a>. $24/night includes space for 2 tents, 2 cars, and 8 people. The Legacy Bike Trail goes all the way from the polo courts to the camp site, about a 12mi/19km bike ride.</p>
                        <p><strong>PARKING:</strong> Car parking at the courts is LIMITED. Please ride your bike to the courts! Lexington is easy to navigate on bikes and everything you might need is within biking distance. DO NOT PARK CARS IN THE GRASS AT COOLAVIN.</p>
                        <p><strong>FOOD &amp; DRINK:</strong> There are several restaurants, breweries, and bars near the courts that will be providing discounts with your wristband. Lexington Bike Polo will be providing coffee, water, and light snacks on site. We aim to have a food truck present for a couple of hours each day as an on-site lunch option.</p>
                        <p><strong>AIRPORTS &amp; SHUTTLES:</strong> The nearest airports are <a href="https://goo.gl/maps/bYGaKcTkQ3T2">LEX</a>, <a href="https://goo.gl/maps/6aaqTnvGWJQ2">SDF</a>, and <a href="https://goo.gl/maps/FDWT9tASD8k">CVG</a>. Lexington Bike Polo will offer shuttles from the LEX airport on Monday 10/2, Tuesday 10/3, and Sunday 10/8 (times TBA). Please contact players from surrounding cities if you need a ride from Louisville or Cincinnati as they will likely travel through these cities to get to Lexington.</p>
                        <p><strong>TRAVELING VISA:</strong> If you are planning to visit the USA to play in the tournament and need a visa, we are willing to write official invitations as a registered 501(c)(3) non-profit incorporated in the District of Columbia. Email Mark Aseltine at <strong>mark@nahardcourt.com</strong>.</p>
